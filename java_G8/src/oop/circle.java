package oop;

public class circle {
 private double radius;
 private String color;
 public circle() {
   radius = 1;
   color = "red";
}
 public circle(double r) {
	 this.radius = r;
	 color = "red";
 }
 public double getRadius() {
	 return radius;
 }
 public void setRadius(double radius) {
    this.radius = radius;
 }
 public double getArea () {
	 return radius;
 }
 public double getCircumference() {
	 return radius;
 }
 
 
}